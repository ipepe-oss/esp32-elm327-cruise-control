float absDiff(float a, float b){
    if(a > b){
        return a - b;
    }else{
        return b - a;
    }
}

float numMax(float a, float b){
    if(a > b){
        return a;
    }else{
        return b;
    }
}

float numMin(float a, float b){
    if(a < b){
        return a;
    }else{
        return b;
    }
}
